#!/bin/env bash

# 1. Apply terraform in ../step-03-provisioning first
# 2. Run from this folder
# 3. The script sections have been modified for both output folder and also
#    to use GCP terraform outputs.


TF_OUTPUTS=$(terraform -chdir=../step-03-provisioning output -json)
TF_CP_IPS=$(echo $TF_OUTPUTS | jq -r '.cp_ips.value[]')
TF_NODE_IPS=$(echo $TF_OUTPUTS | jq -r '.node_ips.value[]')
TF_STATIC_IP_LB=$(echo $TF_OUTPUTS | jq -r '.static_ip_lb.value')
TF_PROJECT_ID=$(echo $TF_OUTPUTS | jq -r '.project_id.value')
TF_NUM_CPS=$(echo $TF_OUTPUTS | jq -r '.num_cps.value')
TF_NUM_NODES=$(echo $TF_OUTPUTS | jq -r '.num_nodes.value')

TF_CP_IPS_LIST=$(echo $TF_CP_IPS|tr -s ' ' ',')

gcloud config set project $TF_PROJECT_ID

mkdir -p output
pushd output

# ca
{

cat > ca-config.json <<EOF
{
  "signing": {
    "default": {
      "expiry": "8760h"
    },
    "profiles": {
      "kubernetes": {
        "usages": ["signing", "key encipherment", "server auth", "client auth"],
        "expiry": "8760h"
      }
    }
  }
}
EOF

cat > ca-csr.json <<EOF
{
  "CN": "Kubernetes",
  "key": {
    "algo": "rsa",
    "size": 2048
  },
  "names": [
    {
      "C": "US",
      "L": "Portland",
      "O": "Kubernetes",
      "OU": "CA",
      "ST": "Oregon"
    }
  ]
}
EOF

cfssl gencert -initca ca-csr.json | cfssljson -bare ca

}

# admin

{

cat > admin-csr.json <<EOF
{
  "CN": "admin",
  "key": {
    "algo": "rsa",
    "size": 2048
  },
  "names": [
    {
      "C": "US",
      "L": "Portland",
      "O": "system:masters",
      "OU": "Kubernetes The Hard Way",
      "ST": "Oregon"
    }
  ]
}
EOF

cfssl gencert \
  -ca=ca.pem \
  -ca-key=ca-key.pem \
  -config=ca-config.json \
  -profile=kubernetes \
  admin-csr.json | cfssljson -bare admin

}

# kubelet client certificates

for ((NODE_INDEX = 0; NODE_INDEX < TF_NUM_NODES; NODE_INDEX++ )); do
instance=node$NODE_INDEX
cat > ${instance}-csr.json <<EOF
{
  "CN": "system:node:${instance}",
  "key": {
    "algo": "rsa",
    "size": 2048
  },
  "names": [
    {
      "C": "US",
      "L": "Portland",
      "O": "system:nodes",
      "OU": "Kubernetes The Hard Way",
      "ST": "Oregon"
    }
  ]
}
EOF

EXTERNAL_IP=$(gcloud compute instances describe ${instance} \
  --format 'value(networkInterfaces[0].accessConfigs[0].natIP)')

INTERNAL_IP=$(gcloud compute instances describe ${instance} \
  --format 'value(networkInterfaces[0].networkIP)')

cfssl gencert \
  -ca=ca.pem \
  -ca-key=ca-key.pem \
  -config=ca-config.json \
  -hostname=${instance},${EXTERNAL_IP},${INTERNAL_IP} \
  -profile=kubernetes \
  ${instance}-csr.json | cfssljson -bare ${instance}
done

